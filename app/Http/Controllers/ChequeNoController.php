class ChequeNoController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data['alldata']= Chequeno::where('branch_id', session('branch_id'))->orderBy('id', 'DESC')->get();
        $data['allchequebook']= Chequebook::where('branch_id', session('branch_id'))->get();
        return view('accounts.chequeNo', $data);
    }

